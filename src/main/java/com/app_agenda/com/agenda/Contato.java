package com.app_agenda.com.agenda;

public class Contato {
	
	 private int id;
	    private String nome;
	    private String email;
	    private String celular;
	    private String telefone;
	    private char snFavorito;
	    private char snAtivo;
	   

	    // Getters e Setters

	    public int getId() {
	        return id;
	    }

	    public void setId(int id) {
	        this.id = id;
	    }

	    public String getNome() {
	        return nome;
	    }

	    public void setNome(String nome) {
	        this.nome = nome;
	    }

	    public String getEmail() {
	        return email;
	    }

	    public void setEmail(String email) {
	        this.email = email;
	    }

	    public String getCelular() {
	        return celular;
	    }

	    public void setCelular(String celular) {
	        this.celular = celular;
	    }

	    public String getTelefone() {
	        return telefone;
	    }

	    public void setTelefone(String telefone) {
	        this.telefone = telefone;
	    }

	    public char getSnFavorito() {
	        return snFavorito;
	    }

	    public void setSnFavorito(char snFavorito) {
	        this.snFavorito = snFavorito;
	    }

	    public char getSnAtivo() {
	        return snAtivo;
	    }

	    public void setSnAtivo(char snAtivo) {
	        this.snAtivo = snAtivo;
	    }

	   
	}


